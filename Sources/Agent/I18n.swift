import Foundation

enum CodexVoiceI18n {
    static let supportedLanguages = ["system", "en", "zh-Hans", "zh-Hant", "ja"]
    static let runtimeLanguages = ["en", "zh-Hans", "zh-Hant", "ja"]

    static func normalize(_ value: Any?) -> String {
        guard let text = value as? String, !text.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty else {
            return "system"
        }
        if supportedLanguages.contains(text) {
            return text
        }
        if let mapped = language(fromLocale: text) {
            return mapped
        }
        return "system"
    }

    static func resolved(config: [String: Any]) -> String {
        let configured = normalize(config["ui_language"])
        if configured != "system" {
            return configured
        }
        for identifier in Locale.preferredLanguages {
            if let mapped = language(fromLocale: identifier) {
                return mapped
            }
        }
        return "en"
    }

    static func language(fromLocale identifier: String) -> String? {
        let normalized = identifier.replacingOccurrences(of: "_", with: "-").lowercased()
        if normalized.hasPrefix("ja") {
            return "ja"
        }
        if normalized.hasPrefix("en") {
            return "en"
        }
        if normalized.hasPrefix("zh") {
            if normalized.contains("hant")
                || normalized.contains("-tw")
                || normalized.contains("-hk")
                || normalized.contains("-mo") {
                return "zh-Hant"
            }
            return "zh-Hans"
        }
        return nil
    }

    static func text(_ key: String, config: [String: Any], _ args: [String: String] = [:]) -> String {
        return text(key, language: resolved(config: config), args)
    }

    static func text(_ key: String, language: String, _ args: [String: String] = [:]) -> String {
        let template: String
        if let localized = table[language]?[key] {
            template = localized
        } else if language == "zh-Hant", let simplified = table["zh-Hans"]?[key] {
            template = traditionalize(simplified)
        } else {
            template = table["en"]?[key] ?? key
        }
        return args.reduce(template) { partial, item in
            partial.replacingOccurrences(of: "{\(item.key)}", with: item.value)
        }
    }

